angka_1 = 10
angka_2 = 2

# Penjumlahan ( + )
penjumlahan = angka_1 + angka_2
print(penjumlahan)

# Pengurangan ( - )
pengurangan = angka_1 - angka_2
print(pengurangan)

# Pembagian ( / )
pembagian = angka_1 - angka_2
print(pembagian)

# Perkalian ( * )
perkalian = angka_1 * angka_2
print(perkalian)

# Pembagian tanpa koma ( // )
pembagian_bulat = angka_1 // angka_2
print(pembagian_bulat)

# Pemangkatan ( ** )
pangkat = angka_1**angka_2
print(pangkat)
