# Fungi if
tinggi = 175
if tinggi > 160:
    print("Bisa Masuk Wahana")

# Fungsi else
tinggi = 150
if tinggi > 160:
    print("Bisa Masuk Wahana")
else:
    print("Tidak Bisa Masuk")

# Fungsi elif
tinggi = 180
if tinggi > 160:
    print("Bisa Masuk Wahana")
elif tinggi > 175 and tinggi < 160:
    print("Terlalu Tinggi")
else:
    print("WTH ARE YOU!!")
