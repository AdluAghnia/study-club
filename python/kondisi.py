# Sama dengan ( == )
print("=== Sama Dengan ===")
print(2 == 3)
print(2 == 2)

# Tidak sama dengan

# Lebih dari
print("=== Lebih Dari ===")
print(3 > 2)
print(2 > 3)
print(3 > 3)

# Kurang dari
print("=== Kurang Dari ===")
print(3 < 2)
print(2 < 3)
print(3 < 3)

# Lebih dari sama dengan
print("=== Lebih Dari Sama Dengan ===")
print(3 >= 2)
print(2 >= 3)
print(3 >= 3)

# Kurang dari sama dengan
print("=== Kurang Dari Sama Dengan ===")
print(3 <= 2)
print(2 <= 3)
print(3 <= 3)

# Kondisi Gabungan dengan and | or
print("========== and ==========")
nilai = 77
print(nilai > 75 and nilai < 80)
# Fungsi or (atau)
print("========== or ==========")
nilai = 77
print(nilai > 75 or nilai < 70)
