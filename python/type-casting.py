# Mengkonversi ke Integer
x = int(3.5)  # x akan menjadi 3
y = int("10")  # y akan menjadi 10
z = int(True)  # z akan menjadi 1


# MEngkonversi ke Float
a = float(7)  # a akan menjadi 7.0
b = float("12.34")  # b akan menjadi 12.34
c = float(False)  # c akan menjadi 0.0

# Mengkonversi ke String
p = str(123)  # p akan menjadi "123"
q = str(4.56)  # q akan menjadi "4.56"
r = str(True)  # r akan menjadi "True"

# Mengkonversi ke Boolean
s = bool(1)  # s akan menjadi True
t = bool(0)  # t akan menjadi False
u = bool("Hello")  # u akan menjadi True
v = bool("")  # v akan menjadi False
